module Keyboard(
    input clk,
    input PS2_CLK,
    input PS2_DATA,
    output [7:0]keycode
    );
    
reg CLK50MHZ=0;

always @(posedge(clk))begin
    CLK50MHZ<=~CLK50MHZ;
end

PS2Receiver keyboard (
.clk(CLK50MHZ),
.kclk(PS2_CLK),
.kdata(PS2_DATA),
.keycodeout(keycode[7:0])
);


 
endmodule